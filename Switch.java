public class Switch {
    public static void main (String[] args){

        int opcoes = 0;

        switch (opcoes){
            case 1:
            System.out.println("Abra sua conta");
            break;

            case 2:
            System.out.println("Ver Fatura");
            break;

            case 3:
            System.out.println("Credito Imobiliario");
            break;

            default:
            System.out.println("Escolha uma opção de 1 á 3");
            break;


        }
    }
}
